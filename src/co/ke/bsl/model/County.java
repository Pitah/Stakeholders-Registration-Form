package co.ke.bsl.model;

public class County {
	private String spd_county_id;
	private String name;
	/**
	 * @return the spd_county_id
	 */
	public String getSpd_county_id() {
		return spd_county_id;
	}
	/**
	 * @param spd_county_id the spd_county_id to set
	 */
	public void setSpd_county_id(String spd_county_id) {
		this.spd_county_id = spd_county_id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	

}
